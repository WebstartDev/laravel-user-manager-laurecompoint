    /**
     * Display the specified resource.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show(User $user, Request $request)
    {
        $users = $user->where('id', $user->id)->first();
        return view('/user')->with([
            'user' => $users,
        ]);
       
    }

